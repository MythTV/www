<li>I updated the OSD font rendering to use freetype 2 -- cleaner looking fonts, and one fewer dependency for those distributions that no longer ship freetype 1.
<li>The release date for version 0.6 is now going to be this Saturday, the 12th, unless something major comes up.  Should be a pretty solid release.  List of changes for the interested:
<ul>
<li>Multiple simultaneous recordings (if you have the tuner cards and the cpu required)
<li>Picture-in-picture support
<li>Plug-in video filters
<li>A (text-only for now) setup program
<li>Better support for European style TV (strings in channel numbers, etc)
<li>Added the ability to colorize the Program Guide based on the category of programs
<li>A complete rewrite of the themed menu class, with a much better file format
<li>Visualizations in MythMusic
<li>Save the current playlist in MythMusic across runs of the program
<li>CD Audio playback in MythMusic now uses cdparanoia to play
<li>Bugfixes all around
</ul>
<li>Big list, lots of major changes.  Anyway, at least MythTV and MythMusic will see a 0.6 release on Saturday.  MythWeb and MythGame are less certain of a full-blown release, but I'll probably put out at least pre-release versions of both.
</ul>
