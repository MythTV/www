<ul>
  <li>New release.  Fun stuff.  <a href="http://www.mythtv.info/moin.cgi/NewVersion">Full changelog here</a>, but the major stuff is:
  <ul>
    <li>I'm now only distributing three source packages, instead of eleven.  The new 'mythplugins' package combines all of the add-in modules that used to be distributed separately (mythmusic, mythweather, etc).  There's a top-level configure script to enable/disable/configure all of the plugins.  The new 'myththemes' package contains some of the themes that used to be distributed in the main download (Titivillus, Minimalist-wide, Iulius).  I've split these out to cut down on the size of the main package a bit.  Be sure to grab the themes package if you're using one of those themes, else you won't get the updated version for this release.
    <li>The configure script was greatly enhanced, and there's no more editing of setting.pro to enable/disable certain options.
    <li>Anyway, 0.18's mainly a bugfix/minor feature release.  Check the changelog for specifics.
  </ul>
  <li>Standard release info:
  <ul>
    <li>Grab the release from the  <a href="http://www.mythtv.org/modules.php?name=Downloads">download section</a> and please at least try to read the <a href="http://www.mythtv.org/modules.php?name=MythInstall">docs</a> before asking questions. 
    <li>The <a href="http://www.mythtv.org/modules.php?name=Web_Links&l_op=viewlink&cid=1">binary packages</a> should hopefully be updated to 0.18 soon.
    <li>See this <a href="http://www.mythtv.org/modules.php?name=Web_Links&l_op=viewlink&cid=3">links section</a> for a list of complete linux distributions based around MythTV.
  </ul>
