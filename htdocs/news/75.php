<ul>
  <li>Just thought I'd update the page with some highlights of current development efforts for those not on the mailing list.
  <li><a href="http://untzuntz.com/mythtv/">John Danner</a> has been working on a redesign for the TV Playback/Delete screens.  Go <a href="http://untzuntz.com/mythtv/playback/">here</a> for a bunch of screenshots showing the new code in action.  He's currently working on making it more easily themeable.
  <li>Thor (original mythweb author) has been doing work on mythmusic, and has contributed some rather nifty playlist support.  You can now create/edit/reorder playlists from the music tree,  Playlists can contain other playlists, etc.
  <li>I've been working on redesigning the OSD code, and it's now quite a bit more flexible.  There have been a couple of  nice looking OSD themes posted to the mythtv-dev list that I'll take screenshots of and post later.
  <li>Apparently the <a href="http://ivtv.sf.net/">driver</a> for the newer Hauppauge WinTV-PVR cards is getting quite useable.  So, I just ordered a WinTV PVR-250 with the intention of adding support for it to MythTV..  Well, we'll see how that goes once the hardware gets here.
