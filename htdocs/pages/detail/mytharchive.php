<div id="detail-mytharchive" class="detail">
    <h1>MythArchive</h1>

    <p>
    This plugin allows TV recordings or video files to be burnt to a DVD-Video.
    Just the thing to let a friend watch something they missed - burn a DVD-RW
    and share!
    <i>Please be aware that this may be in violation of copyright laws in some
    countries -- the MythTV developers do not condone copyright infringement
    in any way.</i>
    </p>
    <p>
    The DVD's appearance and operation can be customised with a number of
    different visual themes, some of which have intros, pages of animated
    thumbnails, etc.
    </p>
    <p>
    Recording title, subtitle, time and description are displayed on the
    main menu, chapter menu or recording detail pages.
    </p>
    <p>
    The plugin uses a number of scripts to translate and shrink the files
    so that they will fit onto single or dual-layer DVD media, or an ISO
    image file for later burning.
    </p>
    <p>
    Errors in TV recordings are usually corrected by these scripts when
    they invoke the mythtranscode or ProjectX utilities. A full log of any
    errors can be viewed as the disc is being prepared, or examined later.
    </p>

    <p>
    Read more in the
    <a href="http://www.mythtv.org/wiki/index.php/MythArchive" title="MythTV Wiki:  MythArchive">MythTV Wiki entry for MythArchive</a>
    </p>

