<div id="detail-themes" class="detail">

    <h1>MythTV Themes</h1>

    <p>
    MythTV has a variety of official and third party theme packages that allow
    you to customize your system to fit your current mood, or just to match
    better with the aspect ratio of your 16:9 TV.
    </p>

    <p>
    MythTV allows for two types of theme, so you can control both the overall
    look and feel of MythTV as well as the on screen display (OSD) that is
    overlayed on the screen when watching TV or videos.
    </p>

    <p>
    Though many of the third party themes are actually designed by active
    MythTV developers, the designers often request that we withhold the
    "official" designation until a theme is deemed 100% complete, including
    both 4:3 and 16:9 versions.  Don't be surprised if some of the better
    themes are actually listed as third party themes.  We strongly encourage
    you to check out all of the available themes and choose the ones that
    look best to you.
    </p>

    <p>
    For a complete list of current themes, please see the
    <a href="http://www.mythtv.org/wiki/index.php/Themes" title="MythTV Wiki:  MythTV Themes">MythTV Wiki entry for Themes</a>
    </p>

