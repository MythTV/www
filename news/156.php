<p>0.26 is now available for download. This is a more modest release than those of the recent past with many of the changes being internal reorganisations required to support future developments.</p>

<p>
Notable changes include:
<ul>
<li> HTTP Live Streaming playback and recording </li>
<li> Conversion of internal times to UTC allowing smoother transitions around daylight savings changes </li>
<li> Several performance improvements to the UI </li>
<li> A greater number of translations (nine) are 100% complete for this release with a few more very near to completion. This number should grow by the time of the 0.26.1 point release.  </li>
<li> The usual bug fixes and incremental improvements </li>
</ul>
A more complete list of changes and bug fixes can be found in the <a href="http://www.mythtv.org/wiki/Release_Notes_-_0.26">Release Notes</a>
</p>

