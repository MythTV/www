        </li>
    <li>After considering the submissions made during the 0.22-0.23 development
        cycle, the winner of our MythUI patch competition is John Poet.  John
        submitted a massive rework of MythUI button lists that allow the themer
        to size selected and unselected items differently, allowing the list to
        dynamically resize and adjust.  This cool feature is already used in many
        new themes.  John is the winner of a free year's membership to Schedules
        Direct.
        </li>
    <li>Work continues on MythTV 0.24 at a blistering pace.  Over the past several
        months, features including the HD Audio branch, the MythUI OSD, a complete
        rework of Program Info handling, unencrypted Blu-ray playback, mass metadata
        updating in MythVideo, and the foundations of the MythMusic rewrite have been
        added to trunk.  .24 promises to be a great step forward and we are still on
        track to bring it to you in the early fall.
        </li>
