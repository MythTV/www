<ul>
  <li>Howdy!  This is something that's been in the works for quite a while now, but we are finally on a brand new server, at a brand new host.  I'll let Chris Petersen (aka xris, aka the MythWeb guy) explain more:
  <li><p>
A few months ago, Isaac mentioned to some of the other devs that the
server hosting mythtv.org (and the <a
href="/support">mailing lists</a>, <a
href="https://code.mythtv.org/trac/">svn</a>, etc.) was reaching its
limit. After a fruitless search for a new inexpensive host, Isaac
contacted the <a href="http://www.osuosl.org/">OSU Open Source Lab</a>
to see if they would host MythTV's services and I brought it up as a
sponsorship opportunity at my work, <a
href="http://www.siliconmechanics.com/">Silicon Mechanics</a>, where we
specialize in rackmount servers.
</p>

<p>
SiMech has always been a supporter of open source software, and with two
key employees (Rob Smith aka kormoc and myself) actively involved with
MythTV, it was an easy decision to become an official sponsor and donate
an <a
href="http://www.siliconmechanics.com/c199/products/rackmount-servers/amd.php">Opteron
server</a>.
</p>

<p>
The new server is a <a
href="http://www.siliconmechanics.com/i3949/2u-opteron-server.php">Rackform
nServ A210</a> with a 2-GHz dual-core Opteron and enough RAM, drive
space and expansion slots to keep mythtv.org running smoothly into the
foreseeable future.  It turns out that the folks at <a
href="http://www.osuosl.org/">OSU OSL</a> are fans of MythTV, and they
have generously offered to host the server in their brand new colocation
facility.
</p>
<p>
- <a href="http://www.mythtv.org/wiki/User:Xris">Chris
Petersen</a>
</p>
