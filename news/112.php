<ul>
  <li>Well, at long last, here's the release.  The <a href="http://www.mythtv.info/moin.cgi/UnderDevelopment">full changelog is here</a>, but here are the biggest 'cool' changes:
  <ul>
    <li>Native support for the frontend (ie., almost everything but recording) running on Mac OS X.  I'll probably be buying a Mini myself at some point for an extra frontend in the bedroom, so that's pretty cool.
    <li>"Timestretch".  Though, it's really 'time-compression'.  This allows the user to adjust the playback speed slightly, but keeps the audio at the same pitch.  If you're recording a lot of shows, timestretch lets you get through them faster. =)
    <li>Big improvements to both DVB and HDTV (for the pcHDTV cards) support.  They're much more useable, with less manual configuration.  Also, there's a new-ish HDTV capable card (the Air2PC) that uses the DVB drivers in Linux and is capable of receiving unencrypted broadcasts from cable TV.  I just recently bought one, but haven't had time to set it up yet.
    <li>A firewire capture method was just added, for those with cable boxes capable of firewire output (Motorola DCT-6200 + cousins, SA 3250, etc).
    <li>And brand new this week: <a href="http://www.mythtv.org/mc/wide-mode2.jpg">wide screen/HDTV support in the user interface</a>.   The theme in that screen shot is still under construction, but it's included in the 0.17 release (Minimalist-wide).  
  </ul>
  <li>Standard release info:
  <ul>
    <li>Grab the release from the  <a href="/download">download section</a> and please at least try to read the <a href="/docs">docs</a> before asking questions. 
    <li>The <a href="http://www.mythtv.org/wiki/index.php/Packages">binary packages</a> should hopefully be updated to 0.17 soon.
    <li>See this <a href="/download#other-downloads">links section</a> for a list of complete linux distributions based around MythTV.
  </ul>
