    <p>
    Nuvexport is a perl script that is able to interface with MythTV in order
    to export your recordings in more portable formats like avi and mp4.  You
    can use it to encode files for playback on your iPhone/iPod, or to save
    commercial-free versions of your high definition recordings in a format
    like h.264 that provides a much better quality to file size ratio.
    </p>
