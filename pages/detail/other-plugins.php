    <h1>Other MythTV Plugins</h1>

    <p>
    MythTV also has a variety of plugins that for one reason or another do not
    have enough screenshots or descriptive documentation available to provide a
    full "in detail" article.  However, since they are can often still be a
    part of a great MythTV system, we've included them here so you can learn
    more about them.
    </p>

    <h2>MythControls</h2>
    <p>
    MythTV is a complex application that uses a variety of different key
    bindings to control its interface.  MythControls provides the ability to
    edit these key bindings from within the frontend so you can customize your
    experience to fit your preferences or to accommodate a remote control
    device with limited key mapping functionality.
    </p>
    <p>
    Read more in the
    <a href="http://www.mythtv.org/wiki/MythControls" title="MythTV Wiki:  MythControls">MythTV Wiki entry for MythControls</a>
    </p>

    <h2>MythFlix</h2>
    <p>
    MythFlix is a new plugin that allows you to manage your NetFlix queue from
    within MythTV.
    </p>
    <p>
    Read more in the
    <a href="http://www.mythtv.org/wiki/MythFlix" title="MythTV Wiki:  MythFlix">MythTV Wiki entry for MythFlix</a>
    </p>

    <h2>MythGame</h2>
    <p>
    Integration with MAME, xNES and other emulators to let you play video
    games from with MythTV
    </p>
    <p>
    Read more in the
    <a href="http://www.mythtv.org/wiki/MythGame" title="MythTV Wiki:  MythGame">MythTV Wiki entry for MythGame</a>
    </p>

    <h2>MythMovies</h2>
    <p>
    MythMovies is a new plugin that allows you to look up local movie showtimes
    in case you want take a break from playing with MythTV.
    </p>
    <p>
    Read more in the
    <a href="http://www.mythtv.org/wiki/MythMovies" title="MythTV Wiki:  MythMovies">MythTV Wiki entry for MythMovies</a>.
    </p>

    <h2>MythWeather</h2>
    <p>
    Display information about local weather, including current temperatures,
    forecasts, and radar maps.
    </p>
    <p>
    Read more in the
    <a href="http://www.mythtv.org/wiki/MythWeather" title="MythTV Wiki:  MythWeather">MythTV Wiki entry for MythWeather</a>
    </p>

    <h2>MythZoneMinder</h2>
    <p>
    Link up to your ZoneMinder video surveilance system and browse all of
    your cameras from the comfort of your couch.
    </p>
    <p>
    Read more in the
    <a href="http://www.mythtv.org/wiki/MythZoneMinder" title="MythTV Wiki:  MythZoneMinder">MythTV Wiki entry for MythZoneMinder</a>
